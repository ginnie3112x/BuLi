package loeckmann.buli;

public class Ergebnis {

	private int erg1;
	private int erg2;

	public Ergebnis(int erg1, int erg2) {
		super();
		this.erg1 = erg1;
		this.erg2 = erg2;
	}

	public int getErg1() {
		return erg1;
	}

	public void setErg1(int erg1) {
		this.erg1 = erg1;
	}

	public int getErg2() {
		return erg2;
	}

	public void setErg2(int erg2) {
		this.erg2 = erg2;
	}

}
